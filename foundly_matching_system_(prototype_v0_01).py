# load the NLP model
# use 'all-MiniLM-L6-v2
from sentence_transformers import SentenceTransformer, util

model_nlp = SentenceTransformer("all-MiniLM-L6-v2")

# Simulation payload text from nodejs (asumtion)
TEMP_TEXT_LOST_ITEM = "Nama: Earphone Wireless. Kategori: Elektronik. Lokasi: Sekitaran kantin kampus. Deskripsi: Earphone lenovo warna hitam."
TEMP_TEXT_FOUND_ITEM = "Nama: Earphone Wireless. Kategori: Elektronik. Lokasi: Meja di Lorong Laboratorium. Deskripsi: Earphone wireless Lenovo warna hitam ditemukan pada meja Laboratorium."

vector_lost_item = model_nlp.encode(TEMP_TEXT_LOST_ITEM, convert_to_tensor=True)
vector_found_item = model_nlp.encode(TEMP_TEXT_FOUND_ITEM, convert_to_tensor=True)

text_score = util.cos_sim(vector_lost_item, vector_found_item)
text_persentage_score = text_score.item() * 100

print(f"TEXT SCORE: {text_persentage_score:.2f}%")

import json

IMG_WEIGHT = 0.60
TEXT_WEIGHT = 0.40

img_score = score_persentage
text_score = text_persentage_score

final_score = (img_score * IMG_WEIGHT) + (text_score * TEXT_WEIGHT)

# Rule Based Filtering (asumtion payload from node js response)
TEMP_LOST_ITEM_CATEGORY = "Elektronik"
TEMP_FOUND_ITEM_CATEGORY = "Elektronik"

# Var initialization
match_status = False
reason_msg = ""

if TEMP_LOST_ITEM_CATEGORY.lower() != TEMP_FOUND_ITEM_CATEGORY.lower():
  final_score = 0.0
  reason_msg = "GAGAL: Kategori barang berbeda"
elif final_score >= MINIMUM_SCORE:
  match_status = True
  reason_msg = "SUKSES: Skor memenuhi standar akurasi Foundly."
else:
  match_status = False
  reason_msg = f"GAGAL: Skor akhir ({final_score:.2f}%) di bawah batas minimal ({MINIMUM_SCORE}%)."

# Format output
output_response = {
    "matching_id": 102,
    "lost_item_id": 45,
    "found_item_id": 46,
    "score_detail": {
        "visual_persentage": round(img_score, 2),
        "text_persentage": round(text_score, 2),
    },
    "final_score_persentage": round(final_score, 2),
    "is_match": match_status,
    "notes": reason_msg
}

print(f"FINAL RESPONSE")
print(json.dumps(output_response, indent=4))

